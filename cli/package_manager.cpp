PackageInstallResult PackageManager::install(const std::string& packageSpec, bool /*globalMode*/) const {
    if (packageSpec.empty()) {
        throw std::runtime_error("Package name must not be empty.");
    }

    const auto cacheRoot = packageCacheDirectory();
    const auto packagesDirectory = projectPackagesDirectory();
    writer_.createDirectory(cacheRoot);
    writer_.createDirectory(packagesDirectory);

    const auto corePackages = corePackageRegistry();
    std::unordered_set<std::string> installing;

    std::function<PackageInstallResult(const std::string&, bool)> installPackage =
        [&](const std::string& spec, bool topLevel) -> PackageInstallResult {
        const auto localSourcePath = resolveLocalSourcePath(spec);
        PackageManifest manifest;
        std::filesystem::path cachePath;
        bool createdPlaceholder = false;
        bool installedFromCore = false;

        if (localSourcePath.has_value()) {
            std::string packageName = packageNameFromSpec(spec);
            if (packageName.empty()) {
                packageName = sanitizePackageName(localSourcePath->filename().string());
            }
            if (packageName.empty()) {
                throw std::runtime_error("Unable to determine a package name from: " + spec);
            }

            std::error_code error;
            manifest = std::filesystem::is_directory(*localSourcePath, error)
                           ? readManifest(*localSourcePath, packageName, kScaffoldVersion, "local")
                           : PackageManifest {packageName, kScaffoldVersion, "local", "", "", "", false, {}};
            manifest.name = packageName;
            cachePath = cacheRoot / manifest.name / manifest.version;
            removeDirectoryIfExists(cachePath);
            writer_.createDirectory(cachePath);

            if (std::filesystem::is_directory(*localSourcePath, error) && !error) {
                copyDirectoryContents(*localSourcePath, cachePath);
            } else if (std::filesystem::is_regular_file(*localSourcePath, error) && !error) {
                writer_.writeTextFile(cachePath / "main.wev", readFileContents(*localSourcePath));
            } else {
                throw std::runtime_error("Unsupported package source: " + localSourcePath->string());
            }

            ensureMainFile(writer_, cachePath, manifest, "");
            writeManifest(writer_, cachePath, manifest);
            ensureReadmeFile(writer_, cachePath, manifest);
        } else {
            const auto requested = parsePackageSpec(spec);
            if (requested.name.empty()) {
                throw std::runtime_error("Unable to determine a package name from: " + spec);
            }

            const std::string installKey =
                requested.name + "@" + (requested.versionConstraint.empty() ? "*" : requested.versionConstraint);
            if (!installing.insert(installKey).second) {
                return PackageInstallResult {};
            }

            const auto registryManifest = registryPackageManifest(requested.name);
            const auto coreFound = corePackages.find(requested.name);
            if (registryManifest.has_value()) {
                if (!requested.versionConstraint.empty() &&
                    !versionMatches(requested.versionConstraint, registryManifest->version)) {
                    throw std::runtime_error("Registry package " + requested.name + " does not satisfy version " +
                                             requested.versionConstraint + ".");
                }

                manifest = *registryManifest;
                cachePath = cacheRoot / manifest.name / manifest.version;

                if (manifest.source.rfind("core://", 0) == 0) {
                    installedFromCore = true;
                    const std::string coreName = manifest.source.substr(7);
                    if (coreFound == corePackages.end()) {
                        throw std::runtime_error("Registry requested unknown core package: " + coreName);
                    }
                    manifest = coreFound->second;
                    cachePath = cacheRoot / manifest.name / manifest.version;
                    removeDirectoryIfExists(cachePath);
                    refreshOfficialPackageFiles(writer_, cachePath, manifest);
                } else if (isHttpUrl(manifest.source)) {
                    if (!std::filesystem::exists(cachePath / "main.wev")) {
                        writer_.createDirectory(cachePath);
                        if (!fetchUrlToFile(manifest.source, cachePath / "main.wev")) {
                            throw std::runtime_error("Unable to fetch package source from registry: " + manifest.source);
                        }
                        writeManifest(writer_, cachePath, manifest);
                        ensureReadmeFile(writer_, cachePath, manifest);
                    }
                } else {
                    std::filesystem::path registrySourcePath = manifest.source;
                    if (manifest.source.rfind("file://", 0) == 0) {
                        registrySourcePath = manifest.source.substr(7);
                    }

                    std::error_code registryError;
                    registrySourcePath = std::filesystem::absolute(registrySourcePath, registryError);
                    if (registryError) {
                        throw std::runtime_error("Unable to resolve registry package source: " + manifest.source);
                    }

                    removeDirectoryIfExists(cachePath);
                    writer_.createDirectory(cachePath);
                    if (std::filesystem::exists(registrySourcePath, registryError) &&
                        std::filesystem::is_directory(registrySourcePath, registryError) && !registryError) {
                        copyDirectoryContents(registrySourcePath, cachePath);
                    } else if (std::filesystem::exists(registrySourcePath, registryError) &&
                               std::filesystem::is_regular_file(registrySourcePath, registryError) && !registryError) {
                        writer_.writeTextFile(cachePath / "main.wev", readFileContents(registrySourcePath));
                    } else {
                        throw std::runtime_error("Registry source does not exist: " + registrySourcePath.string());
                    }
                    writeManifest(writer_, cachePath, manifest);
                    ensureReadmeFile(writer_, cachePath, manifest);
                }
            } else {
                const auto availableVersions = cachedPackageVersions(cacheRoot, requested.name);
                const std::string selectedVersion = pickBestVersion(availableVersions, requested.versionConstraint);
                if (!selectedVersion.empty()) {
                    cachePath = cacheRoot / requested.name / selectedVersion;
                    manifest = readManifest(cachePath, requested.name, selectedVersion, "cache");
                } else {
                    const std::string scaffoldVersion =
                        requested.versionConstraint.empty() ? std::string(kScaffoldVersion) : requested.versionConstraint;
                    manifest = PackageManifest {requested.name,
                                               scaffoldVersion,
                                               "scaffold",
                                               "Scaffold package generated locally by WevoaWeb.",
                                               "",
                                               "",
                                               false,
                                               {}};
                    cachePath = cacheRoot / manifest.name / manifest.version;
                    removeDirectoryIfExists(cachePath);
                    writer_.createDirectory(cachePath);
                    writer_.writeTextFile(cachePath / "main.wev", placeholderPackageTemplate(manifest.name));
                    writeManifest(writer_, cachePath, manifest);
                    ensureReadmeFile(writer_, cachePath, manifest);
                    createdPlaceholder = topLevel;
                }
            }

            ensureMainFile(writer_, cachePath, manifest, installedFromCore ? corePackageSource(manifest.name) : "");
            ensureReadmeFile(writer_, cachePath, manifest);
            PackageInstallResult result {manifest.name, manifest.version, {}, cachePath, createdPlaceholder,
                                         installedFromCore, {}, {}};

            for (const auto& [dependencyName, dependencyVersion] : manifest.dependencies) {
                const auto dependencyResult = installPackage(dependencyName + "@" + dependencyVersion, false);
                if (!dependencyResult.packageName.empty()) {
                    result.installedDependencies.push_back(dependencyResult.packageName + "@" + dependencyResult.version);
                }
            }

            result.destination = packagesDirectory / manifest.name;
            std::error_code destinationError;
            if (std::filesystem::exists(result.destination, destinationError) && !destinationError) {
                const auto existing = readManifest(result.destination, manifest.name, "", "installed");
                if (!existing.version.empty() && existing.version != manifest.version) {
                    result.warnings.push_back("Replacing " + manifest.name + "@" + existing.version + " with " +
                                              manifest.version + ".");
                }
            }
            removeDirectoryIfExists(result.destination);
            copyDirectoryContents(cachePath, result.destination);
            refreshOfficialPackageFiles(writer_, result.destination, manifest);
            ensureMainFile(writer_, result.destination,
                           manifest,
                           installedFromCore ? corePackageSource(manifest.name) : "");
            writeManifest(writer_, result.destination, manifest);
            ensureReadmeFile(writer_, result.destination, manifest);
            updateProjectConfig(writer_, manifest);
            installing.erase(installKey);
            return result;
        }

        PackageInstallResult result {manifest.name, manifest.version, packagesDirectory / manifest.name, cachePath,
                                     createdPlaceholder, false, {}, {}};
        for (const auto& [dependencyName, dependencyVersion] : manifest.dependencies) {
            const auto dependencyResult = installPackage(dependencyName + "@" + dependencyVersion, false);
            if (!dependencyResult.packageName.empty()) {
                result.installedDependencies.push_back(dependencyResult.packageName + "@" + dependencyResult.version);
            }
        }

        std::error_code destinationError;
        if (std::filesystem::exists(result.destination, destinationError) && !destinationError) {
            const auto existing = readManifest(result.destination, manifest.name, "", "installed");
            if (!existing.version.empty() && existing.version != manifest.version) {
                result.warnings.push_back("Replacing " + manifest.name + "@" + existing.version + " with " +
                                          manifest.version + ".");
            }
        }
        removeDirectoryIfExists(result.destination);
        copyDirectoryContents(cachePath, result.destination);
        ensureMainFile(writer_, result.destination, manifest, "");
        writeManifest(writer_, result.destination, manifest);
        ensureReadmeFile(writer_, result.destination, manifest);
        updateProjectConfig(writer_, manifest);
        return result;
    };

    return installPackage(packageSpec, true);
}

PackageRemoveResult PackageManager::remove(const std::string& packageName) const {
    const std::string normalizedName = packageNameFromSpec(packageName);
    if (normalizedName.empty()) {
        throw std::runtime_error("Package name must not be empty.");
    }

    const auto destination = projectPackagesDirectory() / normalizedName;
    std::error_code error;
    if (!std::filesystem::exists(destination, error) || error) {
        throw std::runtime_error("Package is not installed: " + normalizedName);
    }

    removeDirectoryIfExists(destination);
    removeFromProjectConfig(writer_, normalizedName);
    return PackageRemoveResult {normalizedName, destination};
}

std::vector<InstalledPackageInfo> PackageManager::listInstalled() const {
    namespace fs = std::filesystem;

    std::unordered_map<std::string, InstalledPackageInfo> packagesByName;
    for (auto package : packagesFromConfig(fs::current_path() / "wevoa.config.json")) {
        package.path = projectPackagesDirectory() / package.packageName;
        packagesByName.insert_or_assign(package.packageName, std::move(package));
    }

    std::error_code error;
    const auto packagesDirectory = projectPackagesDirectory();
    if (fs::exists(packagesDirectory, error) && fs::is_directory(packagesDirectory, error) && !error) {
        for (fs::directory_iterator iterator(packagesDirectory, fs::directory_options::skip_permission_denied, error), end;
             iterator != end;
             iterator.increment(error)) {
            if (error) {
                throw std::runtime_error("Unable to inspect installed packages in: " + packagesDirectory.string());
            }

            if (!iterator->is_directory(error) || error) {
                continue;
            }

            const auto package = packageInfoFromManifest(iterator->path());
            packagesByName.insert_or_assign(package.packageName, package);
        }
    }

    std::vector<InstalledPackageInfo> packages;
    packages.reserve(packagesByName.size());
    for (auto& entry : packagesByName) {
        packages.push_back(std::move(entry.second));
    }

    std::sort(packages.begin(), packages.end(), [](const InstalledPackageInfo& left, const InstalledPackageInfo& right) {
        return left.packageName < right.packageName;
    });
    return packages;
}

std::vector<InstalledPackageInfo> PackageManager::listAvailableCore() const {
    std::vector<InstalledPackageInfo> packages;
    for (const auto& entry : corePackageRegistry()) {
        const auto& manifest = entry.second;
        std::vector<std::string> dependencies;
        for (const auto& dependency : manifest.dependencies) {
            dependencies.push_back(dependency.first + "@" + dependency.second);
        }
        packages.push_back(InstalledPackageInfo {manifest.name,
                                                 manifest.version,
                                                 manifest.source,
                                                 manifest.description,
                                                 manifest.repo,
                                                 manifest.usageSnippet,
                                                 std::move(dependencies),
                                                 manifest.isOfficial,
                                                 {}});
    }

    std::sort(packages.begin(), packages.end(), [](const InstalledPackageInfo& left, const InstalledPackageInfo& right) {
        return left.packageName < right.packageName;
    });
    return packages;
}

std::vector<InstalledPackageInfo> PackageManager::search(const std::string& query) const {
    const std::string normalizedQuery = sanitizePackageName(query);
    std::vector<InstalledPackageInfo> matches;
    for (const auto& package : registryPackages()) {
        const std::string haystack = sanitizePackageName(package.packageName + " " + package.description + " " + package.source);
        if (normalizedQuery.empty() || haystack.find(normalizedQuery) != std::string::npos) {
            matches.push_back(package);
        }
    }

    std::sort(matches.begin(), matches.end(), [](const InstalledPackageInfo& left, const InstalledPackageInfo& right) {
        if (left.isOfficial != right.isOfficial) {
            // FIXED: Removes redundant !right check. If they are not equal, then left being true implies right is false.
            return left.isOfficial;
        }
        return left.packageName < right.packageName;
    });
    return matches;
}

InstalledPackageInfo PackageManager::packageInfo(const std::string& packageName) const {
    const std::string normalizedName = sanitizePackageName(packageName);
    if (normalizedName.empty()) {
        throw std::runtime_error("Package name must not be empty.");
    }

    for (const auto& package : listInstalled()) {
        if (package.packageName == normalizedName) {
            return package;
        }
    }

    for (const auto& package : registryPackages()) {
        if (package.packageName == normalizedName) {
            return package;
        }
    }

    throw std::runtime_error("Package not found: " + normalizedName);
}

std::string PackageManager::packageNameFromSpec(const std::string& packageSpec) const {
    const auto localPath = resolveLocalSourcePath(packageSpec);
    if (localPath.has_value()) {
        std::string name = localPath->filename().string();
        if (name.size() >= 4 && name.substr(name.size() - 4) == ".wev") {
            name = std::filesystem::path(name).stem().string();
        }
        return sanitizePackageName(name);
    }

    return parsePackageSpec(packageSpec).name;
}

void PackageManager::copyDirectoryContents(const std::filesystem::path& source,
                                           const std::filesystem::path& destination) const {
    namespace fs = std::filesystem;

    writer_.createDirectory(destination);
    std::error_code error;
    for (fs::recursive_directory_iterator iterator(source, fs::directory_options::skip_permission_denied, error), end;
         iterator != end;
         iterator.increment(error)) {
        if (error) {
            throw std::runtime_error("Unable to copy package source: " + source.string());
        }

        if (!iterator->is_regular_file(error) || error) {
            continue;
        }

        const auto relative = fs::relative(iterator->path(), source, error);
        if (error) {
            throw std::runtime_error("Unable to resolve package file path: " + iterator->path().string());
        }

        writer_.writeTextFile(destination / relative, readFileContents(iterator->path()));
    }
}

