std::string readFileContents(const std::filesystem::path& path) {
    std::ifstream stream(path, std::ios::binary);
    if (!stream) {
        throw std::runtime_error("Unable to read package source: " + path.string());
    }

    return std::string(std::istreambuf_iterator<char>(stream), std::istreambuf_iterator<char>());
}

std::string placeholderPackageTemplate(const std::string& packageName) {
    std::string functionName = packageName;
    std::replace(functionName.begin(), functionName.end(), '-', '_');
    return "// Package scaffold generated by WevoaWeb\n"
           "// Replace this with reusable routes, helpers, or components.\n\n"
           "func " +
           functionName +
           "_ready() {\n"
           "  return true\n"
           "}\n";
}

