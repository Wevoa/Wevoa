std::string normalizeImportName(std::string value) {
    if (!value.empty() && value.front() == '@') {
        value.erase(value.begin());
    }
    return value;
}

std::string sanitizePackageName(const std::string& value) {
    std::string sanitized;
    sanitized.reserve(value.size());
    for (const char ch : normalizeImportName(value)) {
        const auto unsignedChar = static_cast<unsigned char>(ch);
        if (std::isalnum(unsignedChar) != 0 || ch == '_' || ch == '-') {
            sanitized.push_back(static_cast<char>(std::tolower(unsignedChar)));
        } else if (ch == '/' || ch == '\\' || std::isspace(unsignedChar) != 0) {
            if (!sanitized.empty() && sanitized.back() != '-') {
                sanitized.push_back('-');
            }
        }
    }

    while (!sanitized.empty() && sanitized.back() == '-') {
        sanitized.pop_back();
    }
    return sanitized;
}

