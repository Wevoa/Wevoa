Value loadJsonObjectOrDefault(const std::filesystem::path& path) {
    std::error_code error;
    if (!std::filesystem::exists(path, error) || error) {
        return Value(Value::Object {});
    }

    try {
        Value value = loadConfigFile(path);
        return value.isObject() ? value : Value(Value::Object {});
    } catch (...) {
        return Value(Value::Object {});
    }
}

Value manifestToValue(const PackageManifest& manifest) {
    Value::Object object;
    object.insert_or_assign("name", Value(manifest.name));
    object.insert_or_assign("version", Value(manifest.version));
    object.insert_or_assign("source", Value(manifest.source));
    if (!manifest.description.empty()) {
        object.insert_or_assign("description", Value(manifest.description));
    }
    if (!manifest.repo.empty()) {
        object.insert_or_assign("repo", Value(manifest.repo));
    }
    if (!manifest.usageSnippet.empty()) {
        object.insert_or_assign("usage", Value(manifest.usageSnippet));
    }
    object.insert_or_assign("isOfficial", Value(manifest.isOfficial));

    Value::Object dependencies;
    for (const auto& [name, version] : manifest.dependencies) {
        dependencies.insert_or_assign(name, Value(version));
    }
    object.insert_or_assign("dependencies", Value(std::move(dependencies)));
    return Value(std::move(object));
}

PackageManifest manifestFromValue(const Value& value,
                                  const std::string& fallbackName,
                                  const std::string& fallbackVersion,
                                  const std::string& fallbackSource) {
    PackageManifest manifest {fallbackName, fallbackVersion, fallbackSource, "", "", "", false, {}};
    if (!value.isObject()) {
        return manifest;
    }

    const auto& object = value.asObject();
    if (const auto found = object.find("name"); found != object.end() && found->second.isString()) {
        manifest.name = sanitizePackageName(found->second.asString());
    }
    if (const auto found = object.find("version"); found != object.end() && found->second.isString()) {
        manifest.version = found->second.asString();
    }
    if (const auto found = object.find("source"); found != object.end() && found->second.isString()) {
        manifest.source = found->second.asString();
    }
    if (const auto found = object.find("description"); found != object.end() && found->second.isString()) {
        manifest.description = found->second.asString();
    }
    if (const auto found = object.find("repo"); found != object.end() && found->second.isString()) {
        manifest.repo = found->second.asString();
    }
    if (const auto found = object.find("usage"); found != object.end() && found->second.isString()) {
        manifest.usageSnippet = found->second.asString();
    }
    if (const auto found = object.find("isOfficial"); found != object.end() && found->second.isBoolean()) {
        manifest.isOfficial = found->second.asBoolean();
    }
    if (const auto found = object.find("dependencies");
        found != object.end() && found->second.isObject()) {
        for (const auto& [name, version] : found->second.asObject()) {
            manifest.dependencies.insert_or_assign(sanitizePackageName(name), version.toString());
        }
    }

    return manifest;
}

std::optional<SemanticVersion> parseSemanticVersion(const std::string& value) {
    SemanticVersion version;
    std::size_t offset = 0;

    const auto parsePart = [&](int& part) -> bool {
        if (offset >= value.size()) {
            return false;
        }

        std::size_t consumed = 0;
        try {
            part = std::stoi(value.substr(offset), &consumed, 10);
            offset += consumed;
            return consumed > 0;
        } catch (...) {
            return false;
        }
    };

    if (!parsePart(version.major)) {
        return std::nullopt;
    }
    if (offset >= value.size() || value[offset] != '.') {
        return std::nullopt;
    }
    ++offset;
    if (!parsePart(version.minor)) {
        return std::nullopt;
    }
    if (offset >= value.size() || value[offset] != '.') {
        return std::nullopt;
    }
    ++offset;
    if (!parsePart(version.patch)) {
        return std::nullopt;
    }

    return offset == value.size() ? std::optional<SemanticVersion>(version) : std::nullopt;
}

int compareSemanticVersions(const SemanticVersion& left, const SemanticVersion& right) {
    if (left.major != right.major) {
        return left.major < right.major ? -1 : 1;
    }
    if (left.minor != right.minor) {
        return left.minor < right.minor ? -1 : 1;
    }
    if (left.patch != right.patch) {
        return left.patch < right.patch ? -1 : 1;
    }
    return 0;
}

bool versionMatches(const std::string& constraint, const std::string& version) {
    if (constraint.empty() || constraint == "*" || constraint == "latest") {
        return true;
    }

    std::string normalized = constraint;
    // FIXED: Removes redundant array non-empty check since constraint check preceding this guarantees it.
    if (normalized.front() == '=') {
        normalized.erase(normalized.begin());
    }

    if (!normalized.empty() && normalized.front() == '^') {
        const auto minimum = parseSemanticVersion(normalized.substr(1));
        const auto candidate = parseSemanticVersion(version);
        if (!minimum.has_value() || !candidate.has_value()) {
            return version == normalized.substr(1);
        }

        return candidate->major == minimum->major && compareSemanticVersions(*candidate, *minimum) >= 0;
    }

    return version == normalized;
}

std::string pickBestVersion(const std::vector<std::string>& versions, const std::string& constraint) {
    std::vector<std::string> matches;
    std::copy_if(versions.begin(), versions.end(), std::back_inserter(matches), [&](const std::string& candidate) {
        return versionMatches(constraint, candidate);
    });

    if (matches.empty()) {
        return "";
    }

    std::sort(matches.begin(), matches.end(), [](const std::string& left, const std::string& right) {
        const auto leftSemver = parseSemanticVersion(left);
        const auto rightSemver = parseSemanticVersion(right);
        if (leftSemver.has_value() && rightSemver.has_value()) {
            return compareSemanticVersions(*leftSemver, *rightSemver) < 0;
        }
        return left < right;
    });

    return matches.back();
}

PackageSpec parsePackageSpec(const std::string& packageSpec) {
    const std::string normalized = normalizeImportName(packageSpec);
    const auto at = normalized.find('@');
    if (at == std::string::npos) {
        return PackageSpec {sanitizePackageName(normalized), ""};
    }

    return PackageSpec {sanitizePackageName(normalized.substr(0, at)), normalized.substr(at + 1)};
}

std::optional<std::filesystem::path> resolveLocalSourcePath(const std::string& packageSpec) {
    std::error_code error;
    const auto absolute = std::filesystem::absolute(packageSpec, error);
    if (error) {
        return std::nullopt;
    }

    if (std::filesystem::exists(absolute, error) && !error) {
        return absolute;
    }

    return std::nullopt;
}

void removeDirectoryIfExists(const std::filesystem::path& path) {
    std::error_code error;
    if (std::filesystem::exists(path, error) && !error) {
        std::filesystem::remove_all(path, error);
        if (error) {
            throw std::runtime_error("Unable to remove directory: " + path.string());
        }
    }
}

